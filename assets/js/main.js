const wallpapers = [
  'wallpaper1.jpg',
  'wallpaper2.jpg',
  'wallpaper3.jpg',
  // Add more wallpapers here
];

// Select the gallery div
const galleryContainer = document.querySelector('.row'); // Select the row where items will be added

// Dynamically add wallpapers to the gallery
wallpapers.forEach(wallpaper => {
  // Create a new column div
  const colDiv = document.createElement('div');
  colDiv.className = 'col-xl-3 col-lg-3 col-md-4 col-6';

  // Create the gallery item div
  const galleryItem = document.createElement('div');
  galleryItem.className = 'gallery-item h-100';

  // Create the image element
  const img = document.createElement('img');
  img.src = `assets/img/wallpapers/${wallpaper}`;
  img.alt = wallpaper;
  img.className = 'img-fluid';

  // Create the download button
  const downloadButton = document.createElement('button');
  downloadButton.className = 'btn btn-download';
  downloadButton.innerHTML = `<i class="bi bi-download"></i> Download`;
  downloadButton.onclick = () => {
    window.location.href = `assets/img/wallpapers/${wallpaper}`;
  };

  // Append elements to the gallery item
  galleryItem.appendChild(img);
  galleryItem.appendChild(downloadButton);

  // Append gallery item to the column
  colDiv.appendChild(galleryItem);

  // Append the column to the gallery container
  galleryContainer.appendChild(colDiv);
});
